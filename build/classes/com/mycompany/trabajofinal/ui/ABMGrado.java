    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed
        Grado grado = new Grado();
        Docente docente = new Docente();
        Persona persona = new Persona();
        Cargo cargo = new Cargo();
        Turno turno = new Turno();
        
        
        int numero = (Integer.parseInt(txtNumero.getText()));
        String seccion = txtSeccion.getText();
        
        Integer idDocente = docentes.get(cbDocentes.getSelectedIndex()-1).getId_docente();
        Integer legajo = docentes.get(cbDocentes.getSelectedIndex()-1).getLegajo();
        
        persona = docentes.get(cbDocentes.getSelectedIndex()-1).getPersona();
        
        cargo = docentes.get(cbDocentes.getSelectedIndex()-1).getCargo();
        
        Integer idTurno = turnos.get(cbTurnos.getSelectedIndex()-1).getId_turno();
        Integer codigo = turnos.get(cbTurnos.getSelectedIndex()-1).getCodigo();
        String descripcion = turnos.get(cbDocentes.getSelectedIndex()-1).getDescripcion();
        
        docente.setId_docente(idDocente);
        docente.setLegajo(legajo);
        docente.setPersona(persona);
        docente.setCargo(cargo);
        
        turno.setId_turno(idTurno);
        turno.setCodigo(codigo);
        turno.setDescripcion(descripcion);
        
        
        grado.setNumero(numero);
        grado.setSeccion(seccion);
        grado.setDocente(docente);
        grado.setTurno(turno);
        gradoControlador.insertarGrado(grado, docente, turno, tabla);
//        if (descripcion.length() < 3)
//            JOptionPane.showMessageDialog(this, "Debe ingresar más de 3 caracteres");
//        else if (codigo == 0 )
//            JOptionPane.showMessageDialog(this, "Debe ingresar un número que no sea 0");
//        else
//            
    }//GEN-LAST:event_btnAgregarActionPerformed

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        Grado grado = new Grado();
        int row = tabla.getSelectedRow();
        String id = tabla.getValueAt(row, 0).toString();
        grado.setId_grado(Integer.parseInt(id));
        
        gradoControlador.eliminarGrado(grado, tabla);
        
        limpiarCampos();
        btnEliminar.setEnabled(false);
    }//GEN-LAST:event_btnEliminarActionPerformed

